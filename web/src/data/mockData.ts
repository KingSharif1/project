function generateHistoricalTrips(): Trip[] {
  const historicalTrips: Trip[] = [];
  const statuses: Trip['status'][] = ['completed', 'completed', 'completed', 'cancelled', 'no-show', 'completed'];
  const tripTypes: Trip['tripType'][] = ['clinic', 'dialysis', 'private'];
  const drivers = ['1', '2', '3', '4'];
  const patients = ['P001', 'P002', 'P003', 'P004', 'P005'];

  for (let i = 0; i < 60; i++) {
    const daysAgo = Math.floor(i / 2);
    const scheduledTime = new Date(Date.now() - daysAgo * 24 * 60 * 60 * 1000);
    const status = statuses[i % statuses.length];
    const fare = 20 + Math.random() * 40;

    historicalTrips.push({
      id: `hist-${i}`,
      driverId: drivers[i % drivers.length],
      patientId: patients[i % patients.length],
      customerName: `Patient ${i % patients.length + 1}`,
      customerPhone: `+1-555-${1000 + i}`,
      customerEmail: `patient${i}@email.com`,
      pickupLocation: `${100 + i} Main St, New York, NY`,
      pickup: `${100 + i} Main St, New York, NY`,
      pickupLatitude: 40.7128 + (Math.random() - 0.5) * 0.1,
      pickupLongitude: -74.0060 + (Math.random() - 0.5) * 0.1,
      dropoffLocation: `${200 + i} Park Ave, New York, NY`,
      dropoff: `${200 + i} Park Ave, New York, NY`,
      dropoffLatitude: 40.7589 + (Math.random() - 0.5) * 0.1,
      dropoffLongitude: -73.9851 + (Math.random() - 0.5) * 0.1,
      status,
      tripType: tripTypes[i % tripTypes.length],
      journeyType: 'one-way',
      serviceLevel: 'ambulatory',
      scheduledTime: scheduledTime.toISOString(),
      pickupTime: status === 'completed' ? new Date(scheduledTime.getTime() + 10 * 60000).toISOString() : undefined,
      dropoffTime: status === 'completed' ? new Date(scheduledTime.getTime() + 40 * 60000).toISOString() : undefined,
      fare: parseFloat(fare.toFixed(2)),
      distance: 2 + Math.random() * 4,
      clinicId: `clinic-${(i % 4) + 1}`,
      paymentStatus: status === 'completed' ? 'paid' : 'pending',
      paymentMethod: 'insurance',
      createdBy: `dispatcher-${(i % 2) + 1}`,
      assignedBy: `dispatcher-${(i % 2) + 1}`,
      createdAt: new Date(scheduledTime.getTime() - 60 * 60000).toISOString(),
      updatedAt: scheduledTime.toISOString(),
    });
  }

  return historicalTrips;
}

